export function t(key, language = 'hi') {
  const translation = translations[key];
  if (!translation) return key;
  return translation[language] || translation['hi'] || key;
}

export function getLanguageLabel(lang) {
  const labels = {
    hi: 'हिंदी',
    bhojpuri: 'भोजपुरी',
    marathi: 'मराठी',
    tamil: 'தமிழ்',
    english: 'English',
    hinglish: 'Hinglish'
  };
  return labels[lang] || 'हिंदी';
}
